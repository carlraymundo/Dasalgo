
package model;
public class location {
	private String sPostNames, sLocation, sDestination, sDistance ;

	public String getsPostNames() {
		return sPostNames;
	}

	public void setsPostNames(String sPostNames) {
		this.sPostNames = sPostNames;
	}

	public String getsLocation() {
		return sLocation;
	}

	public void setsLocation(String sLocation) {
		this.sLocation = sLocation;
	}

	public String getsDestination() {
		return sDestination;
	}

	public void setsDestination(String sDestination) {
		this.sDestination = sDestination;
	}

	public String getsDistance() {
		return sDistance;
	}

	public void setsDistance(String sDistance) {
		this.sDistance = sDistance;
	}
		
	
	
}
